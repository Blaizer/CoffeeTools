#define MOD_VERSION_MAJOR 1
#define MOD_VERSION_MINOR 3
#define MOD_VERSION_BUILD 4

#define GAME_VERSION_MAJOR 1
#define GAME_VERSION_MINOR 7
#define GAME_VERSION_BUILD 8
#define GAME_VERSION_REVSN 21

#define STRINGIZEI(x) #x
#define STRINGIZE(x)  STRINGIZEI(x)

#define STRINGIZE_VERSION3(a,b,c)   STRINGIZE(a) "." STRINGIZE(b) "." STRINGIZE(c)
#define STRINGIZE_VERSION4(a,b,c,d) STRINGIZE(a) "." STRINGIZE(b) "." STRINGIZE(c) "." STRINGIZE(d)

#define MOD_VERSION  STRINGIZE_VERSION3(MOD_VERSION_MAJOR, MOD_VERSION_MINOR, MOD_VERSION_BUILD)
#define GAME_VERSION STRINGIZE_VERSION4(GAME_VERSION_MAJOR, GAME_VERSION_MINOR, GAME_VERSION_BUILD, GAME_VERSION_REVSN)
